<?php
namespace GDI\Dominio\Polizas;

use GDI\Dominio\Oficinas\Oficina;
use GDI\Dominio\Personas\Persona;
use GDI\Dominio\Personas\TipoPersona;
use GDI\Dominio\Personas\Domicilio;

/**
 * Class AsociadoProtegido
 * @package GDI\Dominio\Polizas
 * @author Gerardo Adrián Gómez Ruiz
 * @version 1.0
 */
class AsociadoProtegido extends Persona
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $razonSocial;

    /**
     * @var string
     */
    private $rfc;

    /**
     * @var Domicilio
     */
    private $domicilio;

    /**
     * @var TipoPersona
     */
    private $tipoPersona;

    /**
     * @var Oficina
     */
    private $oficina;

    /**
     * AsociadoProtegido constructor.
     * @param null $razonSocial
     * @param string $rfc
     * @param Domicilio $domicilio
     * @param TipoPersona $tipoPersona
     * @param int $id
     */
    public function __construct($razonSocial = null, $rfc = null, Domicilio $domicilio = null, TipoPersona $tipoPersona = null, $id = 0)
    {
        $this->id          = $id;
        $this->razonSocial = $razonSocial;
        $this->rfc         = $rfc;
        $this->domicilio   = $domicilio;
        $this->tipoPersona = $tipoPersona;

        parent::__construct();
    }

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getRfc()
    {
        return $this->rfc;
    }

    /**
     * @return Domicilio
     */
    public function getDomicilio()
    {
        return $this->domicilio;
    }

    /**
     * @return TipoPersona
     */
    public function getTipoPersona()
    {
        return $this->tipoPersona;
    }

    /**
     * @return string
     */
    public function getRazonSocial()
    {
        return $this->razonSocial;
    }

    /**
     * @return Oficina
     */
    public function getOficina()
    {
        return $this->oficina;
    }

    /**
     * override parent´s behaviour
     * @return null|string
     */
    public function nombreCompleto()
    {
        // TODO: Change the autogenerated stub
        if ($this->tipoPersona === TipoPersona::PERSONA_FISICA) {
            return parent::nombreCompleto();
        } else {
            return $this->razonSocial;
        }
    }
}